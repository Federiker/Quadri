Translator.prototype.translate = function (lang) {
    var langValues = null;
    var keys = null;
    var fullName = "";
    var shortName = "";
    switch (lang) {
        case "en":
            langValues = this.englishValues;
            shortName = this.paintingNameEn;
            fullName = "\"" + shortName + "\" by ";
            break;
        case "de":
            langValues = this.germanValues;
            shortName = this.paintingNameDe;
            fullName = "\"" + shortName + "\" von ";
            break;
        case "fr":
            langValues = this.frenchValues;
            shortName = this.paintingNameFr;
            fullName = "\"" + shortName + "\" de ";
            break;
        case "it":
        default:
            langValues = this.italianValues;
            shortName = this.paintingNameIt;
            fullName = "\"" + shortName + "\" di ";
            break;
    }
    fullName += this.painter;
    keys = Object.keys(langValues);
    for (var i = 0; i < keys.length; i++) {
        var el = document.getElementById(keys[i]);
        el.innerHTML = langValues[keys[i]];
    }
    var el2 = document.getElementById(this.nameId);
    el2.innerHTML = shortName;
    var el3 = document.getElementById(this.titleId);
    el3.innerHTML = fullName;
};