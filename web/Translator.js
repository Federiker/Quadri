Translator.prototype.createFakeValues = function () {
    var keys = Object.keys(this.englishValues);
    for (var i = 0; i < keys.length; i++) {
        if (this.englishValues[keys[i]].match(/^<[^>]{1,}>/)) {
            this.germanValues[keys[i]] = this.englishValues[keys[i]].replace(/^(<[^>]{1,}>)/, "$1Ze ");
            this.frenchValues[keys[i]] = this.englishValues[keys[i]].replace(/^(<[^>]{1,}>)/, "$1Le ");
        }
        else {
            this.germanValues[keys[i]] = "Ze " + this.englishValues[keys[i]];
            this.frenchValues[keys[i]] = "Le " + this.englishValues[keys[i]];
        }
        
    }
};
